var service = prompt("Пожалуйста оставьте отзыв");

if (service == "") {
  alert("Оцените пожалуйста!");
} else if (service >= 5) {
  alert("Благодарим за высокую оценку");
} else if (service <= 5) {
  alert("Мы работаем над улучшением сервиса!");
} else {
  alert("Введите правильно !");
}
document.write(service);
